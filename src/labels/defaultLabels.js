export const STORAGE_KEY = 'mixcomps-labels'

export function deepClone(value) {
  return JSON.parse(JSON.stringify(value))
}

export function setNestedValue(obj, path, value) {
  const next = deepClone(obj)
  let current = next
  for (let i = 0; i < path.length - 1; i += 1) {
    current = current[path[i]]
  }
  current[path[path.length - 1]] = value
  return next
}

export function loadStoredLabels() {
  try {
    const raw = localStorage.getItem(STORAGE_KEY)
    if (!raw) return deepClone(defaultLabels)
    return JSON.parse(raw)
  } catch {
    return deepClone(defaultLabels)
  }
}

export function flattenLabels(obj, prefix = []) {
  const entries = []

  for (const [key, value] of Object.entries(obj)) {
    const path = [...prefix, key]

    if (typeof value === 'string') {
      entries.push({ path, value, group: prefix[0] ?? 'root' })
    } else if (Array.isArray(value)) {
      value.forEach((item, index) => {
        if (typeof item === 'string') {
          entries.push({ path: [...path, index], value: item, group: prefix[0] ?? 'root' })
        } else if (item && typeof item === 'object') {
          entries.push(...flattenLabels(item, [...path, index]))
        }
      })
    } else if (value && typeof value === 'object') {
      entries.push(...flattenLabels(value, path))
    }
  }

  return entries
}
